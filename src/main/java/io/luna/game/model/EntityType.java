package io.luna.game.model;

/**
 * An enum representing different entity types.
 *
 * @author lare96 <http://github.org/lare96>
 */
public enum EntityType {

    /**
     * A ground item.
     */
    ITEM,

    /**
     * An object.
     */
    OBJECT,

    /**
     * A player mob.
     */
    PLAYER,

    /**
     * A non-player mob.
     */
    NPC
}
